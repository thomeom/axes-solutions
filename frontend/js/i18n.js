window.I18N = {
  current: window.AXStorage.getItem("ax.lang") || "ru",

  init() {
    document.documentElement.lang = this.current === "kz" ? "kk" : this.current;
    this.apply();

    const select = document.getElementById("lang-select");
    if (select) {
      select.value = this.current;

      select.addEventListener("change", (e) => {
        this.set(e.target.value);
      });
    }
  },

  t(key) {
    const dict = TRANSLATIONS[this.current] || TRANSLATIONS.ru;
    return dict[key] ?? TRANSLATIONS.ru[key] ?? key;
  },

  base(key) {
    return TRANSLATIONS.ru[key] ?? key;
  },

  format(key, values = {}) {
    return Object.entries(values).reduce(
      (text, [name, value]) => text.replaceAll(`{${name}}`, value),
      this.t(key)
    );
  },

  locale() {
    return {
      ru: "ru-RU",
      kz: "kk-KZ",
      en: "en-US"
    }[this.current] || "ru-RU";
  },

  set(lang) {
    if (!TRANSLATIONS[lang]) return;

    this.current = lang;
    window.AXStorage.setItem("ax.lang", lang);

    document.documentElement.lang = lang === "kz" ? "kk" : lang;
    this.apply();
    window.dispatchEvent(new CustomEvent("i18n:change", { detail: { lang } }));
  },

  apply() {
    document.querySelectorAll("[data-i18n]").forEach(el => {
      const key = el.dataset.i18n;
      const value = this.t(key);
      if (value) el.textContent = value;
    });

    document.querySelectorAll("[data-i18n-ph]").forEach(el => {
      const key = el.dataset.i18nPh;
      const value = this.t(key);
      if (value) el.placeholder = value;
    });

    document.querySelectorAll("[data-i18n-aria]").forEach(el => {
      const key = el.dataset.i18nAria;
      const value = this.t(key);
      if (value) el.setAttribute("aria-label", value);
    });

    document.querySelectorAll("[data-i18n-content]").forEach(el => {
      const key = el.dataset.i18nContent;
      const value = this.t(key);
      if (value) el.setAttribute("content", value);
    });

    document.querySelectorAll("[data-i18n-alt]").forEach(el => {
      const key = el.dataset.i18nAlt;
      const value = this.t(key);
      if (value) el.setAttribute("alt", value);
    });

    document.querySelectorAll("[data-i18n-title]").forEach(el => {
      const key = el.dataset.i18nTitle;
      const value = this.t(key);
      if (value) el.setAttribute("title", value);
    });
  }
};
